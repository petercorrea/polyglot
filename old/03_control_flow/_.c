// C - Control Flow

// If-Else Statement
if (1)
{
    // Executes if true
}
else
{
    // Executes if false
}

// Switch Statement
switch (1)
{
case 1:
    // Executes if 1
    // Fall-through allowed unless 'break' is used
    break;
default:
    // Executes if no cases match
    break;
}

// For Loop
for (int i = 0; i < 10; ++i)
{
    // Loop body
}

// While Loop
while (1)
{
    // Loop body
}

// Do-While Loop
do
{
    // Loop body
} while (0);

// Explanation:
// C uses structured control flow with if-else, switch, for, while, and do-while constructs.
// 'break' and 'continue' can be used to exit loops or skip iterations.
// 'goto' exists but is generally discouraged due to making code less maintainable.
