// Rust - Collection Types

// Vector
let mut vec = vec![1, 2, 3];
// Native Methods: push, pop, insert, remove, clear, len, is_empty

// Tuple
let tup = (1, 2, 3);
// Native Methods: None (Tuples are simple, fixed-size collections)

// HashMap
use std::collections::HashMap;
let mut map = HashMap::new();
// Native Methods: insert, get, remove, contains_key, is_empty, len, clear

// HashSet
use std::collections::HashSet;
let mut set = HashSet::new();
// Native Methods: insert, remove, contains, is_empty, len, clear
