// JavaScript - Control Flow

// If-Else Statement
if (true) {
  // Executes if true
} else {
  // Executes if false
}

// Switch Statement
switch ("value") {
  case "value":
    // Executes if 'value'
    // Fall-through allowed unless 'break' is used
    break;
  default:
    // Executes if no cases match
    break;
}

// For Loop
for (let i = 0; i < 10; i++) {
  // Loop body
}

// While Loop
while (true) {
  // Loop body
}

// Explanation:
// JavaScript control flow is similar to C but includes additional constructs like for-of loops.
// 'break' and 'continue' work similarly to C.
// JavaScript also supports 'try-catch' for exception handling.
